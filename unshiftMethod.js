//program to unshift the element using unshift() method
const classNames = ["Arun", "Faiz", "Roshan","Sakshi", "Yashwanth"]; //giving values to the variable
const className = classNames.unshift("Akshaya"); //The unshift() method adds a new element to an array (at the beginning), and "unshifts" older elements:
console.log(className); //The unshift() method returns the new array length.
console.log(classNames); //printing the values with new value value
//end of the program

